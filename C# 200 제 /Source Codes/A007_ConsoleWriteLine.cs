using System;

namespace Practice
{
    public class A007_ConsoleWriteLine
    {
        static void Main(String[] args)
        {
            bool b = true;
            char c = 'A';
            decimal d = 1.234m;     // m 은 decimal 형의 접미사
            double e = 1.23456789;
            float f = 1.234456789f; // f 는 float 형의 접미사
            int i = 1234;
            string s = "Hello";

            Console.WriteLine(b);
            Console.WriteLine(c);
            Console.WriteLine(d);
            Console.WriteLine(e);
            Console.WriteLine(f);
            Console.WriteLine(i);
            Console.WriteLine(s);

        }
    }
}

